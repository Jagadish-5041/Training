var insta = document.querySelector("#insta");
insta.addEventListener('mouseover' , ()=>{
    insta.src = "insta1.jpg";
    insta.style.transform = 'scale(1.2)';
})
insta.addEventListener('mouseout' , ()=>{
    insta.src = "insta.jpg";
    insta.style.transform = 'scale(1)';
})

